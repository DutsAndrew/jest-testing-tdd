function addIt(number1, number2) {
  return (number1*1 + number2*1);
}

function subtractIt(number1, number2) {
  return (number1*1 - number2*1)
}


function multiplyIt(number1, number2) {
  return (number1*1 * number2*1);
}

function divideIt(number1, number2) {
  return (number1*1 / number2*1);
}

export {
  addIt,
  subtractIt,
  multiplyIt,
  divideIt
}